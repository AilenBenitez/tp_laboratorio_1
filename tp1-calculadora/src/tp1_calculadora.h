#ifndef TP1_CALCULADORA_H_


/// @brief realiza una suma con los numeros ingresados anteriormente
/// @param numeroA (el primer ingresado)
/// @param numeroB (el segundo ingresado)
/// @return el resultado de la suma
float sumar(float numeroA, float numeroB);

/// @brief realiza una resta con los numeros ingresados anteriormente
/// @param numeroA (el primer ingresado)
/// @param numeroB (el segundo ingresado)
/// @return el resultado de la resta
float restar(float numeroA, float numeroB);

/// @brief realiza una multiplicacion con los numeros ingresados anteriormente
/// @param numeroA (el primer ingresado)
/// @param numeroB (el segundo ingresado)
/// @return el resultado de la multiplicacion
float multiplicacion(float numeroA, float numeroB);

/// @brief realiza una division con los numeros ingresados anteriormente
/// @param numeroA (el primer ingresado)
/// @param numeroB (el segundo ingresado)
/// @return el resultado de la division
float dividir(float numeroA, float numeroB);

/// @brief realiza el factorial de primer numero ingresado
/// @param numA (el primer ingresado)
/// @return el resultado del factorial
float factorialA(float numA);

/// @brief realiza el factorial de segundo numero ingresado
/// @param numB (el primer ingresado)
/// @return el resultado del factorial
float factorialB(float numB);

#endif /* TP1_CALCULADORA_H_ */
